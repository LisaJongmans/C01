#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int p;

	p = 0;
	p = *a;
	*a = *b;
	*b = p;
}

int		main(void)
{
	int *a;
	int *b;
	int i = 3;
	int j = 4;

	a = &i;
	b = &j;
	ft_swap(a, b);
}
